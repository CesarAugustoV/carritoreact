// operaciones en lote
export const updateManyProductos = async (items) => {
    const batch = writeBatch(db);

    items.forEach(({
        id,
        qty
    }) => {

        

        batch.update(doc(db, "Items", id), {
            stock: increment(-qty)
        })
    })

    batch.commit();
}